/// A class that represents spacing values.
///
/// This class provides a set of predefined spacing values that can be used
/// for consistent spacing in a UI layout.
interface class Spacing {
  final double sp1;
  final double sp2;
  final double sp3;
  final double sp4;
  final double sp5;
  final double sp6;
  final double sp7;
  final double sp8;
  final double sp9;
  final double sp10;
  final double sp11;
  final double sp12;
  final double sp13;
  final double sp14;
  final double sp15;
  final double sp16;
  final double sp17;
  final double sp18;
  final double sp19;
  final double sp20;
  final double sp21;

  /// Creates a new instance of the [Spacing] class.
  ///
  /// The [sp1], [sp2], [sp3], [sp4], [sp5], [sp6], [sp7], [sp8], [sp9], [sp10],
  /// [sp11], [sp12], [sp13], [sp14], [sp15], [sp16], [sp17], [sp18], [sp19],
  /// [sp20], and [sp21] are required parameters that define the spacing values
  /// for the application.
  const Spacing({
    required this.sp1,
    required this.sp2,
    required this.sp3,
    required this.sp4,
    required this.sp5,
    required this.sp6,
    required this.sp7,
    required this.sp8,
    required this.sp9,
    required this.sp10,
    required this.sp11,
    required this.sp12,
    required this.sp13,
    required this.sp14,
    required this.sp15,
    required this.sp16,
    required this.sp17,
    required this.sp18,
    required this.sp19,
    required this.sp20,
    required this.sp21,
  });
}
